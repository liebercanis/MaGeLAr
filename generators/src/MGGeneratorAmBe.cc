void MGGeneratorAmBe::GeneratePrimaryVertex(G4Event *event)
{  
  fEventNumber = event->GetEventID();
  if ((fEventNumber % fReportingFrequency) == 0) {
    MGManager *manager = MGManager::GetMGManager();
    time_t endTime = time( NULL );
    G4double timeDifference = difftime(endTime, manager->GetMGRunAction()->GetStartTime());
    MGLog(trace) << "Processing Event # " << fEventNumber << " at "
                 << timeDifference << " seconds (real time )" << endlog;
  }

  //  This code is to simulate the neutron spectrum from an AmBe source

  //  Set the particle energy
  double prob = G4UniformRand();
  int iLo = 0;
  int iHi = kCDFSize - 1;

  while( !(fSpectrumCDFValue[iLo+1] >= prob && fSpectrumCDFValue[iHi-1] < prob) ) {
    if( fSpectrumCDFValue[(iLo+iHi)/2] < prob ) iLo = (iLo+iHi)/2;
    else iHi = (iLo+iHi)/2;
  }

  double proportion = (prob - fSpectrumCDFValue[iLo]) / (fSpectrumCDFValue[iHi]-fSpectrumCDFValue[iLo]);
  G4double neutronEnergy = (fSpectrumCDFEnergy_MeV[iLo] + proportion*(fSpectrumCDFEnergy_MeV[iHi] - fSpectrumCDFEnergy_MeV[iLo]))*MeV;
  fParticleGun->SetParticleEnergy(neutronEnergy);
  

  //  Set the random direction
  double pz = 2.0*G4UniformRand() - 1.0;
  double sinTheta = sqrt(1.0 - pz*pz);
  double phi = 2.0*pi*G4UniformRand();
  double px = sinTheta*sin(phi);
  double py = sinTheta*cos(phi);
  fParticleGun->SetParticleDefinition(G4Neutron::NeutronDefinition());
  fParticleGun->SetParticlePosition(fPosition);
  fParticleGun->SetParticleMomentumDirection(G4ThreeVector(px,py,pz));
  fParticleGun->GeneratePrimaryVertex(event);

  double gammaEnergy = 0.0;

  if(neutronEnergy > 0.5*MeV && neutronEnergy < 6.2*MeV) { //release 4.44 MeV gamma 
    //  Set the gamma's random direction
    MGLog(trace) << "Neutron Energy: " << neutronEnergy/MeV << " MeV, 4.44 MeV gamma released" << endlog; 
    pz = 2.0*G4UniformRand() - 1.0;
    sinTheta = sqrt(1.0 - pz*pz);
    phi = 2.0*pi*G4UniformRand();
    px = sinTheta*sin(phi);
    py = sinTheta*cos(phi);
    fParticleGun->SetParticleDefinition(G4Gamma::GammaDefinition());
    fParticleGun->SetParticlePosition(fPosition);
    fParticleGun->SetParticleMomentumDirection(G4ThreeVector(px,py,pz));
    // 1.3% FWHM spread per S. Croft NIM A 218 (1989) 103:
    //gammaEnergy = fRandEngine->Gaus(4.43803,0.013*4.43803/2.355 );
    // 46.7 (1.05%) keV sigma per Jing Liu from GERDA
    gammaEnergy = fRandEngine->Gaus(4.43803,0.0467);
    fParticleGun->SetParticleEnergy(gammaEnergy*MeV );
    fParticleGun->GeneratePrimaryVertex(event);
  }
}
