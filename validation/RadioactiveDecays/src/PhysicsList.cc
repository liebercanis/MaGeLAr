PhysicsList::PhysicsList()
: G4VUserPhysicsList()
{
  //add new units for radioActive decays
  // 
  const G4double minute = 60*second;
  const G4double hour   = 60*minute;
  const G4double day    = 24*hour;
  const G4double year   = 365*day;
  new G4UnitDefinition("minute", "min", "Time", minute);
  new G4UnitDefinition("hour",   "h",   "Time", hour);
  new G4UnitDefinition("day",    "d",   "Time", day);
  new G4UnitDefinition("year",   "y",   "Time", year);        
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

PhysicsList::~PhysicsList()
{ }

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void PhysicsList::ConstructParticle()
{
  // pseudo-particles
  G4Geantino::GeantinoDefinition();
  
  // gamma
  G4Gamma::GammaDefinition();

  // leptons
  G4Electron::ElectronDefinition();
  G4Positron::PositronDefinition();

  G4NeutrinoE::NeutrinoEDefinition();
  G4AntiNeutrinoE::AntiNeutrinoEDefinition();
  
  // baryons
  G4Proton::ProtonDefinition();
  G4Neutron::NeutronDefinition();  

  // ions
  G4IonConstructor iConstructor;
  iConstructor.ConstructParticle();  
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void PhysicsList::ConstructProcess()
{
  AddTransportation();
  
  G4RadioactiveDecay* radioactiveDecay = new G4RadioactiveDecay();
  //radioactiveDecay->SetHLThreshold(-1.*s);
  ///radioactiveDecay->SetICM(false);
  ///radioactiveDecay->SetARM(false);
  G4ProcessManager* pmanager = G4GenericIon::GenericIon()->GetProcessManager();  
  pmanager->AddProcess(radioactiveDecay, 0, -1, 1);    

  // See G4.10.03 release notes regarding atomic de-excitation <http://geant4.cern.ch/support/ReleaseNotes4.10.3.html>
  // See also geant4.10.03/source/processes/hadronic/models/radioactive_decay/src/G4RadioactiveDecay.cc::BuildPhysicsTable()
  G4VAtomDeexcitation* atomDeexcitation = new G4UAtomicDeexcitation();
  atomDeexcitation->SetFluo(true);
  atomDeexcitation->SetAuger(true);
  atomDeexcitation->InitialiseAtomicDeexcitation();
  G4LossTableManager* theManager = G4LossTableManager::Instance();
  theManager->SetAtomDeexcitation(atomDeexcitation);
}

